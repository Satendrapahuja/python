#value convert in octal

value_1 = 10
convert_1 = oct(value_1)
print(value_1, f'a in octal', convert_1)

#value convert in hexadecimal

value_2 = 40
convert_2 = hex(value_2)
print(value_2, f'b in hexadecimal', convert_2)

#value convert in binary

value_3 = 100
convert_3 = bin(value_3)
print(value_3, f'is value 3 in binary ', convert_3)

